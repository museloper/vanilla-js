// 변수 미사용
console.log(5 + 3);
console.log(5 - 3);
console.log(5 * 3);
console.log(5 / 3);

console.log(4 + 2);
console.log(4 - 2);
console.log(4 * 2);
console.log(4 / 2);

// const
// 상수는 값을 변경할 수 없다.
const a = 5;
const b = 3;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);

// let
// 변수는 값을 변경할 수 있다.
let c = 5;
let d = 3;

console.log(c + d);
console.log(c - d);
console.log(c * d);
console.log(c / d);

c = 4;
d = 2;

console.log(c + d);
console.log(c - d);
console.log(c * d);
console.log(c / d);
